package syok_with_java;

public class IfElseExample {
	
	public static void main(String[] args) {
			
		// Do addition and print out sum
		int x = 10;
		int y = 3;
		
		if (x < y) {
			System.out.println("x < y");
		} else if (x > y) {
			System.out.println("x > y");
		} else {
			System.out.println("x = y");
		}
		 
		System.out.println();
		
		}
}
